    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCell(withIdentifier: "ItemCell", for: indexPath) as! ItemCell
        
        let item = itemsStore.allItems[indexPath.row]
        
        cell.nameLabel.text = item.name
        cell.serialNumberLabel.text = item.serialNumber
        cell.valueLabel.text = "$\(item.valueInDollars)"
        // Configure the cell...

        return cell
    }
    
    @IBAction func addItems(_ sender: Any) {
        let newItem = itemsStore.createItem()
        if let row = itemsStore.allItems.index(of: newItem){
            let indexPath: IndexPath = IndexPath(row: row, section: 0)
            
            tableView.insertRows(at: [indexPath], with: .automatic)
        
        }
        
    }
    
    override func tableView(_ tableView: UITableView, commit editingStyle: UITableViewCellEditingStyle, forRowAt indexPath: IndexPath) {
        if editingStyle == .delete {
            let item = self.itemsStore.allItems[indexPath.row]
            
            let title = "Delete Item"
            let deleteMessage = "Are you sure you want to delete \(item.name)?"
            
            let ac = UIAlertController(title: title, message: deleteMessage, preferredStyle: .actionSheet)
            let cancelaction = UIAlertAction(title: "Cancel", style: .cancel, handler: nil)
            let deleteAction = UIAlertAction(title: "Delete", style: .destructive, handler: { (action) -> Void in
                self.itemsStore.removeItem(i: indexPath.row)
                tableView.deleteRows(at: [indexPath], with: .automatic)
            })
            
            ac.addAction(cancelaction)
            ac.addAction(deleteAction)
            
            present(ac, animated: true, completion: nil)
        }
    }

    @IBAction func editItems(_ sender: UIButton) {
        if isEditing {
            sender.setTitle("Edit", for: .normal)
            setEditing(false, animated: true)
        } else {
            sender.setTitle("Done", for: .normal)
            setEditing(true, animated: true)
        }
    }
    
    override func tableView(_ tableView: UITableView, moveRowAt sourceIndexPath: IndexPath, to destinationIndexPath: IndexPath) {
        itemsStore.moveItem(from: sourceIndexPath.row, to: destinationIndexPath.row)
        
    }
    
